            <h4>
          
* In Resolution 9740 promulgated on July 10, candidates who will run in the barangay and Sangguniang Kabataan (SK) elections can file their COCs from October 15 to 17 during regular office hours at their respective municipal or city election officers.

<br><br>* Registered voters will elect in October 28
       <br><br> - one punong barangay (barangay captain)
       <br> - seven members of the Sangguniang Barangay (barangay council)

<br><br>* Filipinos between 15 to 17 years old who registered before the poll body for the SK pools will also vote
        <br><br>- one SK Chairperson
        <br>- seven council members.

<br><br>* Qualifications for prospective barangay and SK officials include:
       <br><br> - Filipino citizenship
       <br>- registered voter where he intends to be elected
       <br>- resident of the barangay for at least a year before election day
       <br> - able to read and write.

<br><br>* To qualify, candidates for barangay officials should also at least be 18 years old
<br><br>* SK candidates should be at least 15
<br><br>* SK candidates should be at least 15 years old but less than 18 years old on October 28.
<br><br>*  Government officials who will file their COCs are deemed resigned when the filing of candidacy begins.


